// Helper Functions
export const getAllProjects = () => {
  console.log("getAllProjects called, projects count:", projects.length);
  return projects;
};

export const getProjectBySlug = (slug) => {
  console.log("getProjectBySlug called with slug:", slug);
  console.log(
    "Available slugs:",
    projects.map((p) => p.slug),
  );

  const project = projects.find((p) => p.slug === slug);
  console.log("Found project:", project ? project.shortTitle : "NOT FOUND");

  return project;
};

export const getRelatedProjects = (currentSlug, limit = 3) => {
  console.log("getRelatedProjects called with slug:", currentSlug);
  const related = projects
    .filter((p) => p.slug !== currentSlug)
    .slice(0, limit);
  console.log("Related projects:", related.length);
  return related;
};

export const getCategories = () => {
  const categories = ["all", ...new Set(projects.map((p) => p.category))];
  return categories;
};

export const getProjectsByCategory = (category) => {
  if (category === "all") return projects;
  return projects.filter((p) => p.category === category);
};
